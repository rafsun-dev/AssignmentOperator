package com.company;

public class Main {

    public static void main(String[] args) {

        int x = 20;
        int y = 10;

        x+=y;
        System.out.println("x = "+x);

        x-=y;
        System.out.println("x = "+x);

        x*=y;
        System.out.println("x = "+x);

        x/=y;
        System.out.println("x = "+x);

        x%=y;
        System.out.println("x = "+x);
    }
}
